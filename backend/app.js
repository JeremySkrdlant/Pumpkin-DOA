import express from 'express';
import {ethers} from 'ethers';
import cors from 'cors';

const app = express()
app.use(cors());

var previousUsers = []; 
var transactionInProgress = false; 


let provider = new ethers.JsonRpcProvider("http://127.0.0.1:8545");
let ownerWallet = new ethers.Wallet("0xac0974bec39a17e36ba4a6b4d238ff944bacb478cbed5efcae784d7bf4f2ff80", provider);
let contractAddress = "0x5FbDB2315678afecb367f032d93F642f64180aa3"

app.get('/requestEth/:address', async (request, response) => {
     const {address} = request.params; 
     let result = previousUsers.find((item)=>{
      return item == address
     })

     if (result){
      response.send("already have token")
      return;
     }


     previousUsers.push(address)

     if(transactionInProgress){
      response.send("Error, somebody else getting paid");
      return;
     }

     transactionInProgress = true;
    const tx = await ownerWallet.sendTransaction({
        to: address,
        value: ethers.parseEther("2")
    })
    transactionInProgress = false; 
    response.send({status: "success", value:tx});
})

app.get('/shutTheThingDown', async (request, response) => {
     let contract = new ethers.Contract(contractAddress, abi, ownerWallet); 
     let result = await contract["closeSuggestionPeriod"]()
     console.log(result);
     response.send("contract closed")
})

app.listen(3000, () => {
    console.log(`Server is Listening on 3000`)
})

